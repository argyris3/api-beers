//Brand schema
import mongoose from 'mongoose';
const Schema = mongoose.Schema;

const TypeSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    products: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Product',
      },
    ],
  },
  { timestamps: true }
);

const Type = mongoose.model('Type', TypeSchema);

export default Type;
